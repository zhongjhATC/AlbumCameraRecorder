package com.zhongjh.albumcamerarecorder.camera.entity;

/**
 */
public class BitmapSize {

	private final int width;
	private final int height;

	public BitmapSize(int width, int height) {
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
}
